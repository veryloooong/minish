/* Header file for terminal colours. */

#ifndef COLORS_H
#define COLORS_H

#define COLOR_RESET "\x1B[0m"

#define COLOR_BLACK_NORMAL "\x1B[30m"   /* Black */
#define COLOR_RED_NORMAL "\x1B[31m"     /* Red */
#define COLOR_GREEN_NORMAL "\x1B[32m"   /* Green */
#define COLOR_YELLOW_NORMAL "\x1B[33m"  /* Yellow */
#define COLOR_BLUE_NORMAL "\x1B[34m"    /* Blue */
#define COLOR_MAGENTA_NORMAL "\x1B[35m" /* Magenta */
#define COLOR_CYAN_NORMAL "\x1B[36m"    /* Cyan */
#define COLOR_WHITE_NORMAL "\x1B[37m"   /* White */

#define COLOR_BLACK_BOLD "\x1B[1;30m"   /* Bold Black */
#define COLOR_RED_BOLD "\x1B[1;31m"     /* Bold Red */
#define COLOR_GREEN_BOLD "\x1B[1;32m"   /* Bold Green */
#define COLOR_YELLOW_BOLD "\x1B[1;33m"  /* Yellow */
#define COLOR_BLUE_BOLD "\x1B[1;34m"    /* Blue */
#define COLOR_MAGENTA_BOLD "\x1B[1;35m" /* Magenta */
#define COLOR_CYAN_BOLD "\x1B[1;36m"    /* Cyan */
#define COLOR_WHITE_BOLD "\x1B[1;37m"   /* White */

#define COLOR_BLACK_ITALIC "\x1B[3;30m"   /* Italic Black */
#define COLOR_RED_ITALIC "\x1B[3;31m"     /* Underlined Red */
#define COLOR_GREEN_ITALIC "\x1B[3;32m"   /* Bold Green */
#define COLOR_YELLOW_ITALIC "\x1B[3;33m"  /* Yellow */
#define COLOR_BLUE_ITALIC "\x1B[3;34m"    /* Blue */
#define COLOR_MAGENTA_ITALIC "\x1B[3;35m" /* Magenta */
#define COLOR_CYAN_ITALIC "\x1B[3;36m"    /* Cyan */
#define COLOR_WHITE_ITALIC "\x1B[3;37m"   /* White */

#define COLOR_BLACK_UNDERLINED "\x1B[4;30m"   /* Underlined Black */
#define COLOR_RED_UNDERLINED "\x1B[4;31m"     /* Underlined Red */
#define COLOR_GREEN_UNDERLINED "\x1B[4;32m"   /* Bold Green */
#define COLOR_YELLOW_UNDERLINED "\x1B[4;33m"  /* Yellow */
#define COLOR_BLUE_UNDERLINED "\x1B[4;34m"    /* Blue */
#define COLOR_MAGENTA_UNDERLINED "\x1B[4;35m" /* Magenta */
#define COLOR_CYAN_UNDERLINED "\x1B[4;36m"    /* Cyan */
#define COLOR_WHITE_UNDERLINED "\x1B[4;37m"   /* White */

#endif
